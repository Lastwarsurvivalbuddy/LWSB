function writeBuildingCostData(core, seasons) {
  const seasonExports = seasons.map((s, i) => {
    const num = i + 1;
    return `export const SEASON_${num}_BUILDINGS: BuildingCostEntry[] = ${JSON.stringify(s?.buildings ?? [], null, 2)};`;
  }).join('\n\n');

  const content = `${header('Source: /api/data/buildings/core + /api/data/buildings/season-1 through season-5')}
// lwtBuildingCostData.ts
// Raw building upgrade costs. Strategic priority lives in lwtBuildingData.ts.

export interface BuildingLevel {
  level: number; rss: Record<string, number>; time: string; power: number;
  [key: string]: unknown;
}
export interface BuildingCostEntry { name: string; levels: BuildingLevel[]; }

// ─── CORE BUILDINGS (${core.buildings.length} total) ───────────────────────────────────────────

export const CORE_BUILDINGS: BuildingCostEntry[] = ${JSON.stringify(core.buildings, null, 2)};

// ─── SEASON BUILDINGS ────────────────────────────────────────────────────────

${seasonExports}

// ─── LOOKUP HELPERS ──────────────────────────────────────────────────────────

export function getCoreBuilding(name: string): BuildingCostEntry | undefined {
  return CORE_BUILDINGS.find(b => b.name.toLowerCase().includes(name.toLowerCase()));
}

export function getBuildingLevelCost(buildingName: string, level: number): BuildingLevel | undefined {
  return getCoreBuilding(buildingName)?.levels.find(l => l.level === level);
}

export function buildingUpgradeCost(
  buildingName: string, fromLevel: number, toLevel: number
): { food: number; iron: number; gold: number } {
  const building = getCoreBuilding(buildingName);
  if (!building) return { food: 0, iron: 0, gold: 0 };
  return building.levels
    .filter(l => l.level > fromLevel && l.level <= toLevel)
    .reduce(
      (sum, l) => ({ food: sum.food + (l.rss.food ?? 0), iron: sum.iron + (l.rss.iron ?? 0), gold: sum.gold + (l.rss.gold ?? 0) }),
      { food: 0, iron: 0, gold: 0 }
    );
}

export function getBuildingCostSummary(): string {
  const coreNames = CORE_BUILDINGS.map(b => \`  \${b.name} (\${b.levels.length} levels)\`).join('\\n');
  const s1Names   = SEASON_1_BUILDINGS.map(b => \`  \${b.name} (\${b.levels.length} levels)\`).join('\\n');
  return \`=== BUILDING COST DATA (Powered by cpt-hedge.com) ===

Core buildings (\${CORE_BUILDINGS.length}):
\${coreNames}

Season 1 buildings:
\${s1Names}

Use getBuildingLevelCost(name, level) for a specific level cost.
Use buildingUpgradeCost(name, from, to) for range totals.\`;
}
`;
  writeModule('lwtBuildingCostData.ts', content);
}

