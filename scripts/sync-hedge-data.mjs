function writeT11ArmamentData(t11) {
  const PIECE_NAMES = ['helmet', 'bodyArmor', 'accessories', 'weapon'];
  const STAR_TIERS  = ['base', 'star1', 'star2', 'star3', 'star4'];

  const content = `${header('Source: /api/data/units/t11')}
// lwtT11ArmamentData.ts
// Raw T11 armament upgrade costs per piece and star tier.
// Strategic guide lives in lwtT11Data.ts — do not merge.

export interface ArmamentLevel {
  level: number; materials: number; cores: number; oil?: number;
}
export interface ArmamentStarTier {
  levelStep: number; levels: ArmamentLevel[];
}
export interface ArmamentPiece {
  base: ArmamentStarTier;
  star1: ArmamentStarTier | null;
  star2: ArmamentStarTier | null;
  star3: ArmamentStarTier | null;
  star4: ArmamentStarTier | null;
}

export const T11_ARMAMENT_PIECES: Record<string, ArmamentPiece> = ${JSON.stringify(t11.pieces, null, 2)};

export const T11_ARMAMENT_UNLOCK_REQUIREMENTS: Record<string, { materials: number; cores: number; oil: number } | null> = ${JSON.stringify(t11.unlockRequirements, null, 2)};

export const T11_ARMAMENT_META = ${JSON.stringify(t11.meta, null, 2)};

// ─── AGGREGATE HELPERS ───────────────────────────────────────────────────────

type StarTier = 'base' | 'star1' | 'star2' | 'star3' | 'star4';
const STAR_TIERS = ['base', 'star1', 'star2', 'star3', 'star4'] as const;
const PIECE_NAMES = ['helmet', 'bodyArmor', 'accessories', 'weapon'] as const;

export function pieceResearchCost(pieceName: string): { materials: number; cores: number } {
  const piece = T11_ARMAMENT_PIECES[pieceName];
  if (!piece) return { materials: 0, cores: 0 };
  let materials = 0, cores = 0;
  for (const star of STAR_TIERS) {
    const tier = piece[star];
    if (!tier) continue;
    for (const lvl of tier.levels) { materials += lvl.materials; cores += lvl.cores; }
  }
  return { materials, cores };
}

export function allPiecesStarCost(star: StarTier): { materials: number; cores: number } {
  let materials = 0, cores = 0;
  for (const name of PIECE_NAMES) {
    const tier = T11_ARMAMENT_PIECES[name]?.[star];
    if (!tier) continue;
    for (const lvl of tier.levels) { materials += lvl.materials; cores += lvl.cores; }
  }
  return { materials, cores };
}

export function getT11ArmamentSummary(): string {
  const pieceRows = PIECE_NAMES.map(name => {
    const cost = pieceResearchCost(name);
    return \`  \${name}: \${cost.materials.toLocaleString()} materials + \${cost.cores.toLocaleString()} cores (all stars)\`;
  }).join('\\n');
  const starRows = STAR_TIERS.map(star => {
    const unlock = T11_ARMAMENT_UNLOCK_REQUIREMENTS[star];
    const research = allPiecesStarCost(star);
    if (!research.materials && !unlock) return null;
    const unlockStr = unlock
      ? \` + one-time unlock: \${unlock.materials.toLocaleString()} mats, \${unlock.cores} cores, \${(unlock.oil ?? 0).toLocaleString()} oil\`
      : '';
    return \`  \${star}: all 4 pieces = \${research.materials.toLocaleString()} mats + \${research.cores} cores\${unlockStr}\`;
  }).filter(Boolean).join('\\n');
  return \`=== T11 ARMAMENT COST DATA (Powered by cpt-hedge.com) ===

Research cost by piece (all stars combined):
\${pieceRows}

Research + unlock cost by star tier:
\${starRows}

NOTE: star3 and star4 unlock data not yet available from Hedge.\`;
}
`;
  writeModule('lwtT11ArmamentData.ts', content);
}

