/**
 * sync-hedge-data.mjs
 * Cpt-Hedgehog API → lwtData module generator
 *
 * Usage (Node 18+, no build step needed):
 *   node scripts/sync-hedge-data.mjs
 *
 * Flags:
 *   --force              Re-fetch all endpoints regardless of timestamp
 *   --dry-run            Fetch and log, do not write files
 *   --only=<path>        Sync only a specific endpoint path
 *
 * Attribution: "Powered by cpt-hedge.com" per partnership agreement.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

// ─── CONFIG ──────────────────────────────────────────────────────────────────

const __dirname  = path.dirname(fileURLToPath(import.meta.url));
const BASE_URL   = 'https://cpt-hedge.com';
const API_KEY    = process.env.HEDGE_API_KEY ?? 'h9nqpO2qE2BHxpEfYEOqt189VKzyVeOG';
const OUT_DIR    = path.resolve(__dirname, '../src/lib');
const STATE_FILE = path.resolve(__dirname, '../.hedge-sync-state.json');

const FORCE     = process.argv.includes('--force');
const DRY_RUN   = process.argv.includes('--dry-run');
const ONLY_ARG  = process.argv.find(a => a.startsWith('--only='))?.split('=')[1];

// ─── HELPERS ─────────────────────────────────────────────────────────────────

async function fetchJson(endpoint) {
  const url = `${BASE_URL}${endpoint}`;
  const res = await fetch(url, { headers: { 'x-api-key': API_KEY } });
  if (!res.ok) throw new Error(`Hedge API ${res.status} on ${endpoint}`);
  return res.json();
}

function writeModule(filename, content) {
  const outPath = path.join(OUT_DIR, filename);
  if (DRY_RUN) {
    console.log(`[DRY RUN] Would write: ${outPath} (${content.length} chars)`);
    return;
  }
  fs.writeFileSync(outPath, content, 'utf8');
  console.log(`✅  Wrote: ${filename}`);
}

function loadState() {
  try { return JSON.parse(fs.readFileSync(STATE_FILE, 'utf8')); }
  catch { return {}; }
}

function saveState(state) {
  if (!DRY_RUN) fs.writeFileSync(STATE_FILE, JSON.stringify(state, null, 2), 'utf8');
}

function shouldSync(endpoint, updatedAt, state) {
  if (FORCE) return true;
  if (ONLY_ARG && endpoint !== ONLY_ARG) return false;
  return state[endpoint] !== updatedAt;
}

function header(note) {
  return `// Powered by cpt-hedge.com — data licensed per partnership agreement.
// AUTO-GENERATED by scripts/sync-hedge-data.mjs — DO NOT EDIT BY HAND.
// ${note}
// Re-run sync script after game patches to refresh data.
`;
}

