// ─── MAIN ─────────────────────────────────────────────────────────────────────

async function main() {
  console.log(`\n🚀 Hedge Data Sync — ${new Date().toISOString()}`);
  if (DRY_RUN)  console.log('   [DRY RUN — no files will be written]');
  if (FORCE)    console.log('   [FORCE — ignoring timestamps, re-fetching all]');
  if (ONLY_ARG) console.log(`   [ONLY: ${ONLY_ARG}]`);
  console.log('');

  console.log('📡 Fetching data index…');
  const index = await fetchJson('/api/data');
  const state = loadState();
  const newState = { ...state };

  const tsMap = {};
  for (const ep of index.endpoints) tsMap[ep.path] = ep.updatedAt;

  const needsSync = (p) => shouldSync(p, tsMap[p] ?? '', state);

  // ── Pack data ────────────────────────────────────────────────────────────
  const packPaths = ['/api/data/stores/packs', '/api/data/stores/passes', '/api/data/stores/currencies'];
  if (packPaths.some(needsSync)) {
    console.log('📦 Syncing pack data…');
    const [packs, passes, currencies] = await Promise.all(packPaths.map(fetchJson));
    writePackData(packs, passes, currencies);
    packPaths.forEach(p => { newState[p] = tsMap[p]; });
  } else {
    console.log('⏭️  Pack data up to date.');
  }

  // ── Store item data ───────────────────────────────────────────────────────
  const storePath = '/api/data/stores/stores';
  if (needsSync(storePath)) {
    console.log('🏪 Syncing store item data…');
    const stores = await fetchJson(storePath);
    writeStoreItemData(stores);
    newState[storePath] = tsMap[storePath];
  } else {
    console.log('⏭️  Store item data up to date.');
  }

  // ── Drone data ────────────────────────────────────────────────────────────
  const dronePaths = ['/api/data/drone/parts', '/api/data/drone/chips'];
  if (dronePaths.some(needsSync)) {
    console.log('🤖 Syncing drone data…');
    const [parts, chips] = await Promise.all(dronePaths.map(fetchJson));
    writeDroneData(parts, chips);
    dronePaths.forEach(p => { newState[p] = tsMap[p]; });
  } else {
    console.log('⏭️  Drone data up to date.');
  }

  // ── T11 armament data ─────────────────────────────────────────────────────
  const t11Path = '/api/data/units/t11';
  if (needsSync(t11Path)) {
    console.log('⚔️  Syncing T11 armament data…');
    const t11 = await fetchJson(t11Path);
    writeT11ArmamentData(t11);
    newState[t11Path] = tsMap[t11Path];
  } else {
    console.log('⏭️  T11 armament data up to date.');
  }

  // ── Overlord cost data ────────────────────────────────────────────────────
  const ovPaths = ['/api/data/overlord/training', '/api/data/overlord/promotion', '/api/data/overlord/skills'];
  if (ovPaths.some(needsSync)) {
    console.log('🦍 Syncing Overlord cost data…');
    const [training, promotion, skills] = await Promise.all(ovPaths.map(fetchJson));
    writeOverlordCostData(training, promotion, skills);
    ovPaths.forEach(p => { newState[p] = tsMap[p]; });
  } else {
    console.log('⏭️  Overlord cost data up to date.');
  }

  // ── Building cost data ────────────────────────────────────────────────────
  const bldCorePath    = '/api/data/buildings/core';
  const bldSeasonPaths = [1, 2, 3, 4, 5].map(n => `/api/data/buildings/season-${n}`);
  const allBldPaths    = [bldCorePath, ...bldSeasonPaths];
  if (allBldPaths.some(needsSync)) {
    console.log('🏗️  Syncing building cost data…');
    const [core, ...seasonResults] = await Promise.all([
      fetchJson(`${bldCorePath}?per_page=500`),
      ...bldSeasonPaths.map(p => fetchJson(`${p}?per_page=500`)),
    ]);
    writeBuildingCostData(core, seasonResults);
    allBldPaths.forEach(p => { newState[p] = tsMap[p]; });
  } else {
    console.log('⏭️  Building cost data up to date.');
  }

  // ── Hero cost stub ────────────────────────────────────────────────────────
  const heroStubPath = path.join(OUT_DIR, 'lwtHeroCostData.ts');
  if (!fs.existsSync(heroStubPath)) {
    console.log('⚠️  Hero cost endpoints not deployed — writing stub.');
    writeHeroCostDataStub();
  } else {
    console.log('⏭️  Hero cost stub exists.');
  }

  saveState(newState);

  console.log('\n✅  Sync complete.');
  console.log('📋  Output files in src/lib/:');
  console.log('    lwtPackData.ts');
  console.log('    lwtStoreItemData.ts');
  console.log('    lwtDroneData.ts');
  console.log('    lwtT11ArmamentData.ts');
  console.log('    lwtOverlordCostData.ts');
  console.log('    lwtBuildingCostData.ts');
  console.log('    lwtHeroCostData.ts  ← stub, hero endpoints pending');
  console.log('\n⚠️  Ping Hedge: hero-exp, hero-shards, weapon-shards, gear, skill-medals all 404.');
}

main().catch(err => {
  console.error('\n❌ Sync failed:', err.message);
  process.exit(1);
});
